package com.gazi.Entity;

import javax.persistence.*;

/**
 *
 * @author fakdi
 */
@Entity
@Table(name = "polyclinics")
public class Polyclinic {

    @Id
    @Column(name = "id")
    private int id;

    @Column(name = "name")
    private String name;

    public Polyclinic(String name) {
        this.name = name;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

}
